<?php
/**
 * logout.php - завершение сессии и выход пользователя
 * 
 * Этот файл:
 * 1. Запускает сессию
 * 2. Очищает все переменные сессии
 * 3. Удаляет куку сессии
 * 4. Уничтожает сессию на сервере
 * 5. Перенаправляет на главную страницу
 */

// Начинаем сессию (чтобы иметь доступ к данным сессии)
session_start();

// Очищаем массив сессии - удаляем все переменные
$_SESSION = array();

// Если в настройках PHP используются куки для сессий
if (ini_get("session.use_cookies")) {
    // Получаем текущие параметры куки сессии
    $params = session_get_cookie_params();
    // Удаляем куку сессии, устанавливая время в прошлое
    setcookie(session_name(), '', time() - 42000,
        $params["path"], $params["domain"],
        $params["secure"], $params["httponly"]
    );
}

// Уничтожаем сессию на сервере (удаляем файл с данными сессии)
session_destroy();

// Перенаправляем пользователя на главную страницу (форма станет пустой)
header('Location: index.php');
exit();
?>
